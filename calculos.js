// Soma utiliza o sinal "+"
// Subtração utiliza o sinal "-"
// Multiplicação utiliza o sinal "*"
// Divisão utiliza o sinal "/"
// Subtração utiliza o sinal "-"
// Modulo da divisão ou resto da divisão utiliza o sinal "%"
// Incremento utiliza o sinal ++
// Decremento utiliza o sinal --
var num1 = 2;
var num2 = 3;

document.write("A soma de  " + num1 + " e " + num2 + " é: " + (num1 + num2) + "<br />");
document.write("A subtração de  " + num1 + " e " + num2 + " é: " + (num1 - num2) + "<br />");
document.write("A multiplicação de  " + num1 + " e " + num2 + " é: " + (num1 * num2) + "<br />");
document.write("A divisão de  " + num1 + " e " + num2 + " é: " + (num1 / num2) + "<br />");
document.write("O modulo de  " + num1 + " e " + num2 + " é: " + (num1 % num2) + "<br />");
document.write("O incremento de  " + num1 + " é: " + (++num1) + "<br />");
document.write("O decremento de  " + num1 + " é: " + (--num1) + "<br />");
